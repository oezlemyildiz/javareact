package LecKodlamaio;

public class User {
	int id;
	String userNumber;
	String email;
	String password;
	
	public User() {
		
	}
	
	public User(int id, String userNumber, String email, String password) {
		super();
		this.id = id;
		this.userNumber = userNumber;
		this.email = email;
		this.password = password;
	}
	

}
